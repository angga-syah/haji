export function Sidebar() {
  const pathname = usePathname()
  const { data: currentUser } = useCurrentUser()

  const hasPermission = (item: SidebarItem): boolean => {
    if (!item.permissions) return true
    if (!currentUser?.user) return false
    return item.permissions.includes(currentUser.user.role)
  }

  const isActive = (href: string): boolean => {
    if (href === '/') {
      return pathname === '/'
    }
    return pathname.startsWith(href)
  }

  return (
    <div className="flex h-full w-64 flex-col bg-gray-900">
      {/* Logo */}
      <div className="flex h-16 items-center justify-center border-b border-gray-800">
        <h1 className="text-xl font-bold text-white">Invoice System</h1>
      </div>

      {/* Navigation */}
      <nav className="flex-1 space-y-1 px-2 py-4">
        {sidebarItems
          .filter(hasPermission)
          .map((item) => (
            <Link
              key={item.href}
              href={item.href}
              className={cn(
                'group flex items-center px-2 py-2 text-sm font-medium rounded-md transition-colors',
                isActive(item.href)
                  ? 'bg-gray-800 text-white'
                  : 'text-gray-300 hover:bg-gray-700 hover:text-white'
              )}
            >
              <span className="mr-3 flex-shrink-0">
                {item.icon}
              </span>
              {item.label}
            </Link>
          ))}
      </nav>

      {/* User Info */}
      {currentUser?.user && (
        <div className="border-t border-gray-800 p-4">
          <div className="flex items-center">
            <div className="flex-shrink-0">
              <div className="h-8 w-8 rounded-full bg-gray-600 flex items-center justify-center">
                <span className="text-sm font-medium text-white">
                  {currentUser.user.full_name.charAt(0)}
                </span>
              </div>
            </div>
            <div className="ml-3 flex-1 min-w-0">
              <p className="text-sm font-medium text-white truncate">
                {currentUser.user.full_name}
              </p>
              <p className="text-xs text-gray-400 capitalize">
                {currentUser.user.role.replace('_', ' ')}
              </p>
            </div>
          </div>
        </div>
      )}
    </div>
  )
}